package com.junit.demo.model;

public class User {

    private String userId;

    private String name;

    private String age;

    private String address;

    public String getUserId() {
        return userId;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getAddress() {
        return address;
    }
}
